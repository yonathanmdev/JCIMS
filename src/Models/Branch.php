<?php
namespace App\Models;

use PDO;

class Branch {
    private $db;
    public function __construct($db) { 
        $this->db = $db;
         }

   public function insertBranch($data) {
    // Only open a transaction if nobody else already has one running.
    // This lets insertBranch() work standalone AND as part of a caller's
    // larger transaction (like Organization::create()).
    $ownTransaction = !$this->db->inTransaction();
    if ($ownTransaction) {
        $this->db->beginTransaction();
    }

    try {
        $sql = "INSERT INTO branches (id, organization_id, parent_id, name, alt_name, phone_number, postal_code, level, logo_url, registered_by) 
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
        $stmt = $this->db->prepare($sql);
        $stmt->execute([
            $data['id'], 
            $data['org_id'], 
            $data['parent_id'], 
            $data['name'], 
            $data['alt_name'], 
            $data['phone_number'],
            $data['postal_code'],
            $data['level'], 
            $data['logo_url'], 
            $data['registered_by']
        ]);

        // internal_id auto-generated by MySQL on insert — fetch it
        $internalId = (int) $this->db->lastInsertId();

        // get parent's path (root branches have no parent)
        $parentPath = '/';
        if (!empty($data['parent_id'])) {
            $parentStmt = $this->db->prepare("SELECT path FROM branches WHERE internal_id = ?");
            $parentStmt->execute([$data['parent_id']]);
            $parentPath = $parentStmt->fetchColumn() ?: '/';
        }

        $path = rtrim($parentPath, '/') . '/' . $internalId . '/';

        $updateStmt = $this->db->prepare("UPDATE branches SET path = ? WHERE id = ?");
        $updateStmt->execute([$path, $data['id']]);

        if ($ownTransaction) {
            $this->db->commit();
        }
        return true;

    } catch (\Exception $e) {
        if ($ownTransaction) {
            $this->db->rollBack();
        }
        throw $e;
    }
}
/**
 * የራሴን ቅርንጫፍ ቀጥተኛ ንዑስ ቅርንጫፎች (Immediate Sub-branches) ብቻ ያመጣል
 */
public function getImmediateSubBranches($myBranchId) {
    // parent_id የእኔን ID የመሰሉትን ብቻ ይፈልጋል
    $sql = "SELECT * FROM branches 
            WHERE parent_id = ?  AND status = 'active'
            ORDER BY name ASC";
            
    try {
        $stmt = $this->db->prepare($sql);
        $stmt->execute([$myBranchId]);
        
        return $stmt->fetchAll(\PDO::FETCH_ASSOC);
    } catch (\PDOException $e) {
        error_log("Get immediate sub-branches error: " . $e->getMessage());
        return [];
    }
}

/**
 * የራሴን ቅርንጫፍ ሁሉንም ንዑስ ቅርንጫፎች (Full Hierarchy - ላልተወሰነ ጥልቀት) ያመጣል
 * e.g. ከ Region admin ብናመጣ Zone, Woreda, Kebele ሁሉንም ይመጣል
 */
public function getDescendantBranches($myBranchId) {
    $sql = "SELECT * FROM branches 
            WHERE path LIKE CONCAT((SELECT path FROM branches WHERE id = ?), '%')
              AND id != ?
              AND status = 'active'
            ORDER BY level ASC, name ASC";

    try {
        $stmt = $this->db->prepare($sql);
        $stmt->execute([$myBranchId, $myBranchId]);

        return $stmt->fetchAll(\PDO::FETCH_ASSOC);
    } catch (\PDOException $e) {
        error_log("Get descendant branches error: " . $e->getMessage());
        return [];
    }
}

public function getMainOfficeId($orgId) {
